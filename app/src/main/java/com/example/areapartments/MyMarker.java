package com.example.areapartments;

public class MyMarker {

    private String title, snippt;

    public MyMarker(String title, String snippt) {
        this.title = title;
        this.snippt = snippt;
    }
    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSnippt() {
        return snippt;
    }

    public void setSnippt(String snippt) {
        this.snippt = snippt;
    }

}
